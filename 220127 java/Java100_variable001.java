public class Java100_variable001{
	public static void main(String[] args){
		System.out.println(3 +4);
		
		int a = 3;
		int b = 4;
		System.out.println(a + b);
		
		a = 4;
		System.out.println(a);
		b = 9;
		System.out.println(a + b);
		
		int sum;
		a = 10;
		b = 7;
		sum = a + b;
		System.out.println(sum);
		
	}
}